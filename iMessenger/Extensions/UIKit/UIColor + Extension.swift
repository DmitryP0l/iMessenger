import UIKit

extension UIColor {
    static func buttonRedColor() -> UIColor {
        return #colorLiteral(red: 0.8599731326, green: 0.1256897449, blue: 0.1353026032, alpha: 1)
    }
    
    static func mainWhiteColor() -> UIColor {
        return #colorLiteral(red: 0.921431005, green: 0.9214526415, blue: 0.9214410186, alpha: 1)
    }
    
    static func buttonBlackColor() -> UIColor {
       return #colorLiteral(red: 0.2605186105, green: 0.2605186105, blue: 0.2605186105, alpha: 1)
    }
    
    static func textFieldLight() -> UIColor {
        return #colorLiteral(red: 0.9214347601, green: 0.9214346409, blue: 0.9214347005, alpha: 1)
    }
}
