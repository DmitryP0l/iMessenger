import Foundation
import UIKit

extension UIColor {
    static func buttonRedColor() -> UIColor {
        return #colorLiteral(red: 0.8599731326, green: 0.1256897449, blue: 0.1353026032, alpha: 1)
    }
    
    static func mainWhiteColor() -> UIColor {
        return #colorLiteral(red: 0.9771639705, green: 0.979793489, blue: 0.992647469, alpha: 1)
    }
    
    static let buttonBlackColor: UIColor = #colorLiteral(red: 0.2605186105, green: 0.2605186105, blue: 0.2605186105, alpha: 1)
}
